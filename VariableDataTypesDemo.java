public class VariableDataTypesDemo {
    public static void main(String[] args) {
        // Integer type
        int age = 25;
        
        // Long type
        long population = 8000000000L;
        
        // Double type
        double price = 99.99;
        
        // Character type
        char grade = 'A';
        
        // Boolean type
        boolean isPassed = true;

        System.out.println("== Variable Data Types Demo ==");
        System.out.println("Age (int): " + age);
        System.out.println("World Population (long): " + population);
        System.out.println("Product Price (double): " + price);
        System.out.println("Grade (char): " + grade);
        System.out.println("Passed? (boolean): " + isPassed);
    }
}
